        public static string GetLocationName(double lx, double ly)
        {
            for (int i = 0; i < Zones.Length; i++)
                if (lx >= ((Zone)Zones[i]).points[0] && lx <= ((Zone)Zones[i]).points[3] && ly >= ((Zone)Zones[i]).points[1] && ly <= ((Zone)Zones[i]).points[4])
                    return ((Zone)Zones[i]).name;
            return "";
        }

        public static string getVehicleName(int vehicleid)
        {
            if (vehicleid < 400 || vehicleid - 400 >= VehicleNames.Length)
                return "";
            return VehicleNames[vehicleid - 400];
        }

        public static string getRadioName(int radioid)
        {
            if (radioid < 0 || radioid >= gtaRadioNames.Length)
                return "";
            return gtaRadioNames[radioid];
        }


        public static bool isPlane(int name)
        {
            if ((name >= 400) && (name <= 611))
            {
                foreach (int id in planeIDs)
                {
                    if (id == name)
                    {
                        return true;
                    }
                }
            }
            return false;
        }

        public static int getSeats(int name)
        {
            if ((name >= 400) && (name <= 611))
            {
                return vehicleSeats[name - 400];
            }
            return 0;
        }
        public static Vehicle vehicleType(int type)
        {
            switch (type)
            {
                case 0: return Vehicle.PLANECAR;
                case 5: return Vehicle.BOAT;
                case 6: return Vehicle.TRAIN;
                case 9: return Vehicle.BIKE;
            }
            return Vehicle.ERROR;
        }
        public static string vehicleTypeSting(Vehicle type)
        {
            switch (type)
            {
                case Vehicle.PLANECAR: return "Car/Plane";
                case Vehicle.BOAT: return "Boat";
                case Vehicle.TRAIN: return "Train";
                case Vehicle.BIKE: return "Bike";
            }
            return "Unknown";
        }
